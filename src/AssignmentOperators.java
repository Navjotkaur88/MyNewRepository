public class AssignmentOperators {
    public static void main(String[] args){

      //  int y; //declaration
        System.out.println("Before Swapping");
        int x = 10;
        int y = 22;
        System.out.println(x);
        System.out.println(y);
       // x = x + 20; // x += 20;
        System.out.println("After Swapping");
        int s = x;
        x = y;
        y = s;
        System.out.println(x);
        System.out.println(y);
    }
}

